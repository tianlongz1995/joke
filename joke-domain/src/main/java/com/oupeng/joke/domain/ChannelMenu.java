public class ChannelMenu {
    /** 渠道编号    */
    private Integer i;
    /** 渠道编名称  name  */
    private String n;
    /** 频道类型  type  */
    private String t;

    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }
}
